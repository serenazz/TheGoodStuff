SELECT
	paysuborder.userid,
    payorder.recommenduserid,
    COUNT(*),
    a.r,
    a.r/COUNT(*)
FROM
    paysuborder
LEFT JOIN
    (
    SELECT
        modanb.buyerid as id,
        DATEDIFF(
            FROM_UNIXTIME(modanb.dateline, "%Y-%m-%d"),
            payorder.orderday
        ) AS r
    from modanb
    LEFT JOIN
        payorder
    ON
        payorder.buyerid = modanb.buyerid
    WHERE
        payorder.isneworder = 1
    GROUP BY
        modanb.buyerid
) a
ON
    a.id= paysuborder.userid
LEFT JOIN
    payorder
ON
    payorder.orderid = paysuborder.orderid
WHERE
    payorder.orderday > "2017-03-01"
GROUP BY
    paysuborder.userid
